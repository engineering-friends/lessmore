import stringcase


assert stringcase.camelcase("foo_bar_baz") == "fooBarBaz"
assert stringcase.camelcase("FooBarBaz") == "fooBarBaz"
assert stringcase.capitalcase("foo_bar_baz") == "Foo_bar_baz"
assert stringcase.capitalcase("FooBarBaz") == "FooBarBaz"
assert stringcase.constcase("foo_bar_baz") == "FOO_BAR_BAZ"
assert stringcase.constcase("FooBarBaz") == "FOO_BAR_BAZ"
assert stringcase.lowercase("foo_bar_baz") == "foo_bar_baz"
assert stringcase.lowercase("FooBarBaz") == "foobarbaz"
assert stringcase.pascalcase("foo_bar_baz") == "FooBarBaz"
assert stringcase.pascalcase("FooBarBaz") == "FooBarBaz"
assert stringcase.pathcase("foo_bar_baz") == "foo/bar/baz"
assert stringcase.pathcase("FooBarBaz") == "foo/bar/baz"
assert stringcase.sentencecase("foo_bar_baz") == "Foo bar baz"
assert stringcase.sentencecase("FooBarBaz") == "Foo bar baz"
assert stringcase.snakecase("foo_bar_baz") == "foo_bar_baz"
assert stringcase.snakecase("FooBarBaz") == "foo_bar_baz"
assert stringcase.spinalcase("foo_bar_baz") == "foo-bar-baz"
assert stringcase.spinalcase("FooBarBaz") == "foo-bar-baz"
assert stringcase.titlecase("foo_bar_baz") == "Foo Bar Baz"
assert stringcase.titlecase("FooBarBaz") == "Foo Bar Baz"
assert stringcase.trimcase("foo_bar_baz") == "foo_bar_baz"
assert stringcase.trimcase("FooBarBaz") == "FooBarBaz"
assert stringcase.uppercase("foo_bar_baz") == "FOO_BAR_BAZ"
assert stringcase.uppercase("FooBarBaz") == "FOOBARBAZ"
assert stringcase.alphanumcase("_Foo., Bar") == "_FooBar"
assert stringcase.alphanumcase("Foo_123 Bar!") == "Foo_123Bar"
